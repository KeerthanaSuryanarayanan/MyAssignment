package assignment;

public class PositivetoNegative {
	public static void main(String [] args ) {
		
		int no=-41;
		if(no>1) {
			System.out.println(no+ "-is the Positive number");
			int no1=no*-1;
			System.out.println(no1+ "-is changed to Negative number");
		}
		else if(no<1) { 
			System.out.println(no+ "-is the Negative number");
			int no2=no*-1;
			System.out.println(no2+ "-is changed to Positive number");
		}
		
		
	}
	

}
